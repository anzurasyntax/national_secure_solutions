<script>
    let current = 0;
    const track = document.getElementById('values-track');
    const dots = document.querySelectorAll('.dot');

    function goToSlide(index) {
        current = index;
        const containerWidth = track.parentElement.offsetWidth;
        const cardWidth = (containerWidth - 48) / 3; // 48 = gap-6 * 2 gaps
        track.style.transform = `translateX(-${current * (containerWidth + 24)}px)`;
        dots.forEach((d, i) => {
            if (i === current) {
                d.classList.add('bg-primary', 'w-10');
                d.classList.remove('bg-gray-300');
            } else {
                d.classList.add('bg-gray-300');
                d.classList.remove('bg-primary');
            }
        });
    }

    goToSlide(0);
</script>
